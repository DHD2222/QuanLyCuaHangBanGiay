    private boolean validateForm() {
    if (txtMaKhachHang.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Không được để trống mã khách hàng");
        return false;
    }

    if (txtTenKhachHang.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Không được để trống tên khách hàng");
        return false;
    }

    if (txtDiaChi.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Không được để trống địa chỉ khách hàng");
        return false;
    }

    if (txtSDT.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Không được để trống số điện thoại khách hàng");
        return false;
    }

    // Phone number validation using regular expression
    if (!checkPhoneNumber(txtSDT.getText())) {
        return false; // Phone number format is invalid
    }

    if (txtEmail.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Không được để trống email khách hàng");
        return false;
    }

    // Email validation
    if (!checkEmail(txtEmail.getText())) {
        return false;  // Email format is invalid
    }

    if (txtGhiChu.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Không được để trống ghi chú khách hàng");
        return false;
    }

    return true;
}

// Phone number validation method
private boolean checkPhoneNumber(String phoneNumber) {
    // Regular expression for a valid phone number (10 digits only)
    String phoneRegex = "^\\d{10}$"; // Only 10 digits (no spaces or symbols allowed)

    Pattern phonePattern = Pattern.compile(phoneRegex);
    Matcher matcher = phonePattern.matcher(phoneNumber);
    
    if (!matcher.matches()) {
        JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ. Vui lòng nhập số điện thoại đúng định dạng (10 chữ số).");
        return false;
    }
    return true;
}

// Email validation method
private boolean checkEmail(String email) {
    // Regular expression for validating email format
    String emailRegex = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$";
    Pattern emailPattern = Pattern.compile(emailRegex, Pattern.CASE_INSENSITIVE);
    Matcher matcher = emailPattern.matcher(email);

    if (!matcher.matches()) {
        JOptionPane.showMessageDialog(this, "Email không hợp lệ. Vui lòng nhập email đúng định dạng (ví dụ: example@gmail.com).");
        return false;
    }
    return true;
}
