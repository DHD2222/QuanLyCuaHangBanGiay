List<KhachHang> result = khachHangService.SearchKhachHang(keyword);

if (result.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng với mã hoặc tên: " + keyword);
} else {
    DefaultTableModel model = (DefaultTableModel) tblBangKhachHang.getModel();
    model.setRowCount(0); // Clear the table

    // Add the search results to the table
    for (KhachHang kh : result) {
        model.addRow(kh.toDataRow());  // Assuming `toDataRow` method is available in `KhachHang` class
    }
}
