   private void fillTable(int page) {
        List<KhachHang> list = qlkh.getAllKhachHang(page);
        this.getPage(list);
        dtm = (DefaultTableModel) tblBangKhachHang.getModel();
        dtm.setRowCount(0);

        for (KhachHang khachHang : list) {
            dtm.addRow(khachHang.toDataRow());
        }
    }

