    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed

        if (validateForm()) {
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Bạn có chắc chắn muốn thêm nhân viên này?",
                    "Xác nhận",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION) {
                if (nhanVienService.checktrungma(txtmanhanvien.getText())) {
                    JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại! Vui lòng chọn mã khác.");
                    txtmanhanvien.requestFocus();
                    return;
                }
                NhanVien newNhanVien = readFormngaytao();

                int result = nhanVienService.addNhanVien(newNhanVien);

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                    fillTableNhanVien(currentPage);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }

            } else {

                JOptionPane.showMessageDialog(this, "Hành động thêm nhân viên đã bị hủy.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnthemActionPerformed

