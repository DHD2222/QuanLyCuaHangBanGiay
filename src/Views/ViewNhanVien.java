    private void fillTableNhanVien(int page) {
        List<NhanVien> list = nhanVienService.getAllNhanVien(page);
        this.getPageNhanVien(list);
        dtm = (DefaultTableModel) tblbangnhanvien.getModel();
        dtm.setRowCount(0);
        for (NhanVien nhanVien : list) {
            dtm.addRow(nhanVien.toDataRow());
        }
    }

    void getPageNhanVien(List<NhanVien> listNhanVien) {
        if (listNhanVien.size() % limit == 0) {
            numberPage = listNhanVien.size() / limit;
        } else {
            numberPage = (listNhanVien.size() / limit) + 1;
        }
        lblpage.setText("1"); // Set current page to 1 initially
    }

