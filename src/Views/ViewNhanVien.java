    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed

        String maNhanVien = txttimkiem.getText().trim();

        if (maNhanVien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên để tìm kiếm.");
            return;
        }

        List<NhanVien> result = nhanVienService.searchNhanVienByMa(maNhanVien);

        if (result.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên với mã: " + maNhanVien);
        } else {

            DefaultTableModel model = (DefaultTableModel) tblbangnhanvien.getModel();
            model.setRowCount(0);

            for (NhanVien nv : result) {
                model.addRow(new Object[]{
                    nv.getId(),
                    nv.getMaNhanVien(),
                    nv.getTenNhanVien(),
                    nv.isGioiTinh() ? "Nam" : "Nữ",
                    nv.getNgaySinh(),
                    nv.getDiaChi(),
                    nv.getSoDienThoai(),
                    nv.getTenDangNhap(),
                    nv.getMatKhau(),
                    nv.getVaiTro(),
                    nv.getAnh(),
                    nv.getNgayTao(),
                    nv.getNgaySua(),
                    nv.getTrangThai() == 1 ? "Hoạt động" : "Ngừng hoạt động"
                });
            }
        }
    }//GEN-LAST:event_btntimkiemActionPerformed

