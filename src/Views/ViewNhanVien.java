    private void fillForm(int index) {
        lblidnhanvien.setText(tblbangnhanvien.getValueAt(index, 0).toString());
        txtmanhanvien.setText(tblbangnhanvien.getValueAt(index, 1).toString());
        txthoten.setText(tblbangnhanvien.getValueAt(index, 2).toString());
        String gioiTinh = tblbangnhanvien.getValueAt(index, 3).toString();
        if ("Nam".equals(gioiTinh)) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        txtngaysinh.setText(tblbangnhanvien.getValueAt(index, 4).toString());
        txtdiachi.setText(tblbangnhanvien.getValueAt(index, 5).toString());
        txtsdt.setText(tblbangnhanvien.getValueAt(index, 6).toString());
        txttendangnhap.setText(tblbangnhanvien.getValueAt(index, 7).toString());
        txtmatkhau.setText(tblbangnhanvien.getValueAt(index, 8).toString());

        String vaiTro = tblbangnhanvien.getValueAt(index, 9).toString();
        if ("Nhân viên".equals(vaiTro)) {
            rdonhanvien.setSelected(true);
        } else {
            // Set other radio button based on role
            rdoquanly.setSelected(true);
        }

        lblanh.setText(tblbangnhanvien.getValueAt(index, 10).toString());

        String trangThai = tblbangnhanvien.getValueAt(index, 13).toString();
        if ("Hoat Dong".equals(trangThai)) {
            rdohoatdong.setSelected(true);
        } else {
            rdokhonghoatdong.setSelected(true);
        }
    }

    private NhanVien readFormngaytao() {

        int id = 0;
        String maNhanVien = txtmanhanvien.getText();
        String tenNhanVien = txthoten.getText();
        boolean gioiTinh = rdonam.isSelected();
        Date ngaySinh = null;
        LocalDateTime ldt = LocalDateTime.now();
        Date ngayTao = java.sql.Timestamp.valueOf(ldt);
        try {

            ngaySinh = t.parse(txtngaysinh.getText());
        } catch (ParseException e) {
            e.printStackTrace();
        }

        String diaChi = txtdiachi.getText();
        String soDienThoai = txtsdt.getText();
        String tenDangNhap = txttendangnhap.getText();
        String matKhau = txtmatkhau.getText();
        String vaiTro = "";
        if (rdoquanly.isSelected()) {
            vaiTro = "Quan Ly";
        } else if (rdonhanvien.isSelected()) {
            vaiTro = "Nhan Vien";
        }

        int trangThai = rdohoatdong.isSelected() ? 1 : 0;
        String anh = duongdananh;

        return new NhanVien(maNhanVien, tenNhanVien, gioiTinh, ngaySinh, diaChi, soDienThoai,
                tenDangNhap, matKhau, vaiTro, anh, ngayTao, trangThai);

    }

