    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        if (index >= 0) {

            if (!validateForm()) {
                return;
            }
            check = JOptionPane.showConfirmDialog(this, "Xác nhận sửa dữ liệu?");
            if (check != JOptionPane.YES_OPTION) {
                return;
            }

            try {
                NhanVien nv = readFormngaysua();

                if (nv == null) {
                    return;
                }

                Integer id = Integer.valueOf(lblidnhanvien.getText());

                nhanVienService.updateNhanVien(readFormngaysua(), id);
                this.fillTableNhanVien(currentPage);
                JOptionPane.showMessageDialog(this, "Sửa dữ liệu thành công!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu!");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để sửa.");
        }
    }//GEN-LAST:event_btnsuaActionPerformed
