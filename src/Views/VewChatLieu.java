    private void fillTable(ArrayList<ChatLieu> allChatLieu) {
        dtm = (DefaultTableModel) tblBangChatLieu.getModel();
        dtm.setRowCount(0);
        for (ChatLieu c : allChatLieu) {
            dtm.addRow(new Object[]{c.getId(), c.getMachatLieu(), c.getTenchatLieu(), c.getNguonGoc(), c.getMoTa(), c.getNgayTao(), c.getNgaySua(), c.getTrangThai() == 1 ? "Hoat dong" : "Dung hoat dong"});
        }
    }

    private void fillForm(int index) {
        lblId.setText(tblBangChatLieu.getValueAt(index, 0).toString());
        txtMaChatLieu.setText(tblBangChatLieu.getValueAt(index, 1).toString());
        txtTenChatLieu.setText(tblBangChatLieu.getValueAt(index, 2).toString());
        txtNguonGoc.setText(tblBangChatLieu.getValueAt(index, 3).toString());
        txtMoTa.setText(tblBangChatLieu.getValueAt(index, 4).toString());
        if (tblBangChatLieu.getValueAt(index, 7).equals("Hoat dong")) {
            rdoHoatDong.setSelected(true);
        } else {
            rdoDungHoatDong.setSelected(true);
        }
    }

    private ChatLieu readForm() {
        int a;
        LocalDateTime ldt = LocalDateTime.now();
        Date ngayTao = java.sql.Timestamp.valueOf(ldt);
        if (rdoHoatDong.isSelected()) {
            a = 1;
        } else {
            a = 0;
        }
        return new ChatLieu(txtMaChatLieu.getText(), txtTenChatLieu.getText(), txtNguonGoc.getText(), txtMoTa.getText(), ngayTao, a);
    }

    private ChatLieu readForm1() {
        int a;
        LocalDateTime ldt = LocalDateTime.now();
        Date ngaySua = java.sql.Timestamp.valueOf(ldt);
        if (rdoHoatDong.isSelected()) {
            a = 1;
        } else {
            a = 0;
        }
        return new ChatLieu(txtMaChatLieu.getText(), txtTenChatLieu.getText(), txtNguonGoc.getText(), txtMoTa.getText(), a, ngaySua);
    }

