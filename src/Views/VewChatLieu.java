    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (validateForm()) { 
           
            String maChatLieu = txtMaChatLieu.getText();
            if (qlcl.checkDuplicateMaChatLieu(maChatLieu)) {
                JOptionPane.showMessageDialog(this, "Mã chất liệu đã tồn tại! Vui lòng chọn mã khác.");
                txtMaChatLieu.requestFocus();
                return;
            }
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Bạn có chắc chắn muốn thêm chất liệu này?",
                    "Xác nhận",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                qlcl.newChatLieu(readForm());
                fillTable(qlcl.getAllChatLieu());
                JOptionPane.showMessageDialog(this, "Chất liệu đã được thêm thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm chất liệu bị hủy.");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
     if (index >= 0) {
           if (!validateForm()) {
                return;
            }
int check = JOptionPane.showConfirmDialog(this, "Xác nhận sửa dữ liệu?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            ChatLieu nv = readForm1();

            if (nv == null) {
                return;
            }

            Integer id = Integer.valueOf(lblId.getText());

            qlcl.updateChatLieu(readForm1(), id);
            this.fillTable(qlcl.getAllChatLieu());
            JOptionPane.showMessageDialog(this, "Sửa dữ liệu thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu!");
        }
        } else {
            JOptionPane.showMessageDialog(this, "Hay chon doi tuong");
        }
                 
    }//GEN-LAST:event_btnSuaActionPerformed

